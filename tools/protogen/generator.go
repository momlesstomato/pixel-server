package main

import (
	"fmt"
	"strings"
)

// GoType maps a YAML field type to Go type info.
type GoType struct {
	TypeStr   string
	ReadExpr  string
	WriteExpr string
	IsComplex bool
}

// ResolveType maps a YAML type string to Go type info.
func ResolveType(yamlType string) GoType {
	switch yamlType {
	case "boolean":
		return GoType{"bool", "r.ReadBool()", "w.WriteBool(%s)", false}
	case "int16":
		return GoType{"int16", "r.ReadInt16()", "w.WriteInt16(%s)", false}
	case "uint16":
		return GoType{"uint16", "r.ReadUint16()", "w.WriteUint16(%s)", false}
	case "int32":
		return GoType{"int32", "r.ReadInt32()", "w.WriteInt32(%s)", false}
	case "uint32":
		return GoType{"uint32", "r.ReadUint32()", "w.WriteUint32(%s)", false}
	case "string":
		return GoType{"string", "r.ReadString()", "w.WriteString(%s)", false}
	case "bytes":
		return GoType{"[]byte", "r.ReadBytes()", "w.WriteBytes(%s)", false}
	case "double":
		return GoType{"float64", "r.ReadFloat64()", "w.WriteFloat64(%s)", false}
	case "list<int32>", "int32[]":
		return GoType{"[]int32", "r.ReadListInt32()", "w.WriteListInt32(%s)", false}
	case "list<string>", "string[]":
		return GoType{"[]string", "r.ReadListString()", "w.WriteListString(%s)", false}
	case "int16[]":
		return GoType{"[]int32", "r.ReadListInt32()", "w.WriteListInt32(%s)", false}
	case "object", "list<object>", "object[]":
		return GoType{"[]byte", "r.ReadBytes()", "w.WriteBytes(%s)", true}
	default:
		return GoType{"[]byte", "r.ReadBytes()", "w.WriteBytes(%s)", true}
	}
}

func goFieldName(name string) string {
	if len(name) == 0 {
		return name
	}
	return strings.ToUpper(name[:1]) + name[1:]
}

// titleSegments splits a dotted/underscored name and PascalCases every segment.
func titleSegments(name string) string {
	// Split on dots first, then underscores.
	var sb strings.Builder
	for _, dotPart := range strings.Split(name, ".") {
		for _, seg := range strings.Split(dotPart, "_") {
			if len(seg) > 0 {
				sb.WriteString(strings.ToUpper(seg[:1]))
				sb.WriteString(seg[1:])
			}
		}
	}
	return sb.String()
}

func goStructName(name, dirSuffix string) string {
	return titleSegments(name) + dirSuffix + "Packet"
}

func goConstName(header, dirSuffix string) string {
	parts := strings.Split(header, "_")
	var sb strings.Builder
	sb.WriteString("Header")
	for _, p := range parts {
		if len(p) > 0 {
			sb.WriteString(strings.ToUpper(p[:1]))
			sb.WriteString(strings.ToLower(p[1:]))
		}
	}
	sb.WriteString(dirSuffix)
	return sb.String()
}

func goDecoderName(name, dirSuffix string) string {
	return "Decode" + titleSegments(name) + dirSuffix
}

func realmToFilename(realm string) string {
	return strings.ReplaceAll(realm, "-", "_")
}

// GenerateRealmFile produces Go source for all packets in a realm.
func GenerateRealmFile(pkg, realm string, c2s, s2c []PacketDef) string {
	var sb strings.Builder
	sb.WriteString("// Code generated by protogen. DO NOT EDIT.\n\n")
	sb.WriteString(fmt.Sprintf("package %s\n\n", pkg))
	sb.WriteString("import \"pixel-server/pkg/codec\"\n\n")
	for _, pkt := range c2s {
		generatePacket(&sb, pkt, "In")
	}
	for _, pkt := range s2c {
		generatePacket(&sb, pkt, "Out")
	}
	return sb.String()
}

func generatePacket(sb *strings.Builder, pkt PacketDef, dirSuffix string) {
	constName := goConstName(pkt.Header, dirSuffix)
	structName := goStructName(pkt.Name, dirSuffix)
	decoderName := goDecoderName(pkt.Name, dirSuffix)

	sb.WriteString(fmt.Sprintf("const %s uint16 = %d\n\n", constName, pkt.ID))
	sb.WriteString(fmt.Sprintf("// %s — %s\ntype %s struct {\n", structName, pkt.Summary, structName))
	for _, field := range pkt.Fields {
		gt := ResolveType(field.Type)
		comment := ""
		if gt.IsComplex {
			comment = " // TODO: hand-implement nested decode"
		}
		sb.WriteString(fmt.Sprintf("\t%s %s%s\n", goFieldName(field.Name), gt.TypeStr, comment))
	}
	sb.WriteString("}\n\n")

	sb.WriteString(fmt.Sprintf("func (p *%s) HeaderID() uint16 { return %s }\n\n", structName, constName))

	sb.WriteString(fmt.Sprintf("func (p *%s) Encode(w *codec.Writer) error {\n", structName))
	for _, field := range pkt.Fields {
		gt := ResolveType(field.Type)
		sb.WriteString(fmt.Sprintf("\t"+gt.WriteExpr+"\n", "p."+goFieldName(field.Name)))
	}
	sb.WriteString("\treturn nil\n}\n\n")

	sb.WriteString(fmt.Sprintf("func %s(r *codec.Reader) (*%s, error) {\n", decoderName, structName))
	sb.WriteString(fmt.Sprintf("\tp := &%s{}\n", structName))
	if len(pkt.Fields) > 0 {
		sb.WriteString("\tvar err error\n")
	}
	for _, field := range pkt.Fields {
		gt := ResolveType(field.Type)
		goField := goFieldName(field.Name)
		sb.WriteString(fmt.Sprintf("\tif p.%s, err = %s; err != nil { return nil, err }\n", goField, gt.ReadExpr))
	}
	sb.WriteString("\treturn p, nil\n}\n\n")
}

// GenerateRouter produces router.go.
func GenerateRouter(pkg string, c2s, s2c []PacketDef) string {
	var sb strings.Builder
	sb.WriteString("// Code generated by protogen. DO NOT EDIT.\n\n")
	sb.WriteString(fmt.Sprintf("package %s\n\n", pkg))
	sb.WriteString("import \"pixel-server/pkg/codec\"\n\n")
	sb.WriteString("// HandlerFunc is the type for packet handler callbacks.\n")
	sb.WriteString("type HandlerFunc func(r *codec.Reader) error\n\n")
	sb.WriteString("// C2SRouter maps c2s header IDs to their handler.\n")
	sb.WriteString("var C2SRouter = map[uint16]HandlerFunc{}\n\n")
	sb.WriteString("// RegisterC2SHandler registers a handler for a c2s header ID.\n")
	sb.WriteString("func RegisterC2SHandler(id uint16, h HandlerFunc) {\n\tC2SRouter[id] = h\n}\n\n")
	sb.WriteString("// S2CRouter maps s2c header IDs to their handler.\n")
	sb.WriteString("var S2CRouter = map[uint16]HandlerFunc{}\n\n")
	sb.WriteString("// RegisterS2CHandler registers a handler for a s2c header ID.\n")
	sb.WriteString("func RegisterS2CHandler(id uint16, h HandlerFunc) {\n\tS2CRouter[id] = h\n}\n\n")
	sb.WriteString("// C2SPacketName maps c2s header IDs to human-readable names.\n")
	sb.WriteString("var C2SPacketName = map[uint16]string{\n")
	for _, pkt := range c2s {
		sb.WriteString(fmt.Sprintf("\t%d: %q,\n", pkt.ID, pkt.Name))
	}
	sb.WriteString("}\n\n")
	sb.WriteString("// S2CPacketName maps s2c header IDs to human-readable names.\n")
	sb.WriteString("var S2CPacketName = map[uint16]string{\n")
	for _, pkt := range s2c {
		sb.WriteString(fmt.Sprintf("\t%d: %q,\n", pkt.ID, pkt.Name))
	}
	sb.WriteString("}\n")
	return sb.String()
}
